import math

degrees = 30


radians = math.radians(degrees)


sine_value = math.sin(radians)
cosine_value = math.cos(radians)
tangent_value = math.tan(radians)

print(f"The sine of {degrees} degrees is: {sine_value}")
print(f"The cosine of {degrees} degrees is: {cosine_value}")
print(f"The tangent of {degrees} degrees is: {tangent_value}")